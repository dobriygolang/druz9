package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"maps"
	"os"
	"slices"
	"strings"

	"gopkg.in/yaml.v3"
)

type definition struct {
	Usage string `yaml:"usage"`
}

func main() {
	input := flag.String("input", "", "path to realtime values yaml")
	output := flag.String("output", "", "path to generated go file")
	flag.Parse()

	if strings.TrimSpace(*input) == "" {
		exitf("input is required")
	}
	if strings.TrimSpace(*output) == "" {
		exitf("output is required")
	}

	content, err := os.ReadFile(*input)
	if err != nil {
		exitf("read input: %v", err)
	}

	definitions := make(map[string]definition)
	if err := yaml.Unmarshal(content, &definitions); err != nil {
		exitf("unmarshal yaml: %v", err)
	}

	keys := slices.Sorted(maps.Keys(definitions))

	var buf bytes.Buffer
	buf.WriteString("// Code generated by rtccodegen. DO NOT EDIT.\n")
	buf.WriteString("\n")
	buf.WriteString("package rtc\n")
	buf.WriteString("\n")
	buf.WriteString("const (\n")
	for _, key := range keys {
		name := exportName(key)
		usage := strings.TrimSpace(definitions[key].Usage)
		if usage != "" {
			buf.WriteString(fmt.Sprintf("\t// %s\n", usage))
		}
		buf.WriteString(fmt.Sprintf("\t%s Key = %q\n", name, key))
	}
	buf.WriteString(")\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		exitf("format generated file: %v", err)
	}

	if err := os.WriteFile(*output, formatted, 0o600); err != nil {
		exitf("write output: %v", err)
	}
}

func exportName(key string) string {
	parts := strings.FieldsFunc(key, func(r rune) bool {
		return (r < 'a' || r > 'z') && (r < 'A' || r > 'Z') && (r < '0' || r > '9')
	})

	var builder strings.Builder
	for _, part := range parts {
		if part == "" {
			continue
		}
		builder.WriteString(strings.ToUpper(part[:1]))
		if len(part) > 1 {
			builder.WriteString(strings.ToLower(part[1:]))
		}
	}

	name := builder.String()
	if name == "" {
		return "UnnamedKey"
	}
	if name[0] >= '0' && name[0] <= '9' {
		return "Key" + name
	}
	return name
}

func exitf(format string, args ...any) {
	_, _ = fmt.Fprintf(os.Stderr, format+"\n", args...)
	os.Exit(1)
}
